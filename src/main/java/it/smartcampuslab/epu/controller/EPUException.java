package it.smartcampuslab.epu.controller;


public class EPUException extends Exception {

	public EPUException() {
		super();
	}

	public EPUException(String message, Throwable cause) {
		super(message, cause);
	}

	public EPUException(String message) {
		super(message);
	}

	public EPUException(Throwable cause) {
		super(cause);
	}

}
